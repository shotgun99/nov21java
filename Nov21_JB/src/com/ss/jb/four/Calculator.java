package com.ss.jb.four;

public class Calculator {
	
	public Integer add(Integer i, Integer j) {
		return i+j;
	}
	
	public Integer add(Integer i, Integer j, Integer k) {
		return i+j+k;
	}
	
	public Integer add(Integer i, Integer j, Integer k, Integer l) {
		return i+j+k+l;
	}
	
	public Integer sub(Integer i, Integer j) {
		return i-j;
	}
	
	public Integer sub(Integer i, Integer j, Integer k) {
		return i-j-k;
	}
	
	public Integer sub(Integer i, Integer j, Integer k, Integer l) {
		return i+j-k-l;
	}

}
